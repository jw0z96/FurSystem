#ifndef MESHDATA_H
#define MESHDATA_H

#include "Mesh.h"

#include "nodeeditor/NodeDataModel.hpp"

using QtNodes::NodeData;
using QtNodes::NodeDataType;

class MeshData : public NodeData
{
	public:
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief constructor
		//----------------------------------------------------------------------------------------------------------------------
		MeshData() {}
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief constructor
		//----------------------------------------------------------------------------------------------------------------------
		MeshData(Mesh const &mesh) : _mesh(mesh) {}
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief return the datatype
		//----------------------------------------------------------------------------------------------------------------------
		NodeDataType type() const override {return {"mesh", "M"};};
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief return the data
		//----------------------------------------------------------------------------------------------------------------------
		Mesh pixmap() const {return _mesh;};

	private:
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief _mesh The Mesh object
		//----------------------------------------------------------------------------------------------------------------------
		Mesh _mesh;
};

#endif // MESHDATA_H
