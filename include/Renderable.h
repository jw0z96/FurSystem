#ifndef RENDERABLE_H
#define RENDERABLE_H

enum renderableType
{
	DEFAULT,
	MESH
};

class Renderable
{
	public:
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief constructor
		//----------------------------------------------------------------------------------------------------------------------
		Renderable(){}
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief destructor
		//----------------------------------------------------------------------------------------------------------------------
		~Renderable(){}
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief draw this renderable
		//----------------------------------------------------------------------------------------------------------------------
		virtual void draw(){}
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief get the type of this renderable
		//----------------------------------------------------------------------------------------------------------------------
		virtual renderableType getType() const {return DEFAULT;}
};

#endif // RENDERABLE_H
