#ifndef CURVESDATA_H
#define CURVESDATA_H

#include "Curves.h"

#include "nodeeditor/NodeDataModel.hpp"

using QtNodes::NodeData;
using QtNodes::NodeDataType;

class CurvesData : public NodeData
{
	public:
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief constructor
		//----------------------------------------------------------------------------------------------------------------------
		CurvesData() {}
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief constructor
		//----------------------------------------------------------------------------------------------------------------------
		CurvesData(Curves const &_curves) : m_curves(_curves) {}
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief return the datatype
		//----------------------------------------------------------------------------------------------------------------------
		NodeDataType type() const override {return {"curves", "Curves"};};
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief return the data
		//----------------------------------------------------------------------------------------------------------------------
		Curves curves() const {return m_curves;};

	private:
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief _mesh The Mesh object
		//----------------------------------------------------------------------------------------------------------------------
		Curves m_curves;
};

#endif // CURVESDATA_H
