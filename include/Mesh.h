#ifndef MESH_H
#define MESH_H

#include <glm/glm.hpp>
#include <glm/gtc/matrix_transform.hpp>

#include <string>
#include <iostream>
#include <vector>

struct Vertex
{
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief vertex position
	//----------------------------------------------------------------------------------------------------------------------
	glm::vec3 position;
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief vertex normal
	//----------------------------------------------------------------------------------------------------------------------
	glm::vec3 normal;
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief vertex uv
	//----------------------------------------------------------------------------------------------------------------------
	glm::vec2 uv;
};

struct Mesh
{
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief constructor
	//----------------------------------------------------------------------------------------------------------------------
	Mesh(){}
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief constructor
	//----------------------------------------------------------------------------------------------------------------------
	Mesh(std::vector<Vertex>& _vertices, std::vector<unsigned int>& _indices)
	{
		m_vertices = _vertices;
		m_indices = _indices;
	}
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief m_vertices the vertices that make up the mesh
	//----------------------------------------------------------------------------------------------------------------------
	std::vector<Vertex> m_vertices;
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief m_indices the indices of the vertices
	//----------------------------------------------------------------------------------------------------------------------
	std::vector<unsigned int> m_indices;
};

#endif // MESH_H
